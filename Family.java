
public class Family 
{
	private int a; //IDs of the 4 nodes 
	private int b;
	private int c;
	private int d;
	
	public Family() //constructors
	{
		
	}
	public Family(int a, int b, int c,int d)
	{
		this.a=a;
		this.b=b;
		this.c=c;
		this.d=d;
		
		
		
	}
	//accessors
	public int a()
	{
		return a;
	}
	public int b()
	{
		return b;
	}
	public int c()
	{
		return c;
	}
	public int d()
	{
		return d;
	}
}
